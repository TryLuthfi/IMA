package indonesia.ima.com.ima;

public class User {

    String id_user;
    String nama;
    String username;
    String password;
    String no_telp;
    String email_pribadi;
    String foto_profile;
    String bidang_usaha;

    public User(){

    }

    public String getId_user() {
        return id_user;
    }

    public String getNama() {
        return nama;
    }

    public String getUsername() {
        return username;
    }

    public String getPassword() {
        return password;
    }

    public String getNo_telp() {
        return no_telp;
    }

    public String getEmail_pribadi() {
        return email_pribadi;
    }

    public String getFoto_profile() {
        return foto_profile;
    }

    public String getBidang_usaha() {
        return bidang_usaha;
    }
}


